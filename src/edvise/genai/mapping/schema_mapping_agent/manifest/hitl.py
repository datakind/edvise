class InstitutionSMAHITLItems(BaseModel):
    """
    All SMA HITL items for one institution run.
    Written to: institutions/<institution_id>/sma_hitl.json

    Empty items list means all fields either auto-approved or refined_by_llm —
    no human review needed, gate check passes immediately.
    """

    model_config = ConfigDict(extra="forbid")

    institution_id: str
    entity_type: Literal["cohort", "course"]
    items: list[SMAHITLItem] = Field(default_factory=list)

    @model_validator(mode="after")
    def items_match_institution(self) -> "InstitutionSMAHITLItems":
        for item in self.items:
            if item.institution_id != self.institution_id:
                raise ValueError(
                    f"SMAHITLItem '{item.item_id}' institution_id "
                    f"'{item.institution_id}' does not match envelope "
                    f"institution_id '{self.institution_id}'."
                )
        return self

    @model_validator(mode="after")
    def items_match_entity_type(self) -> "InstitutionSMAHITLItems":
        for item in self.items:
            if item.entity_type != self.entity_type:
                raise ValueError(
                    f"SMAHITLItem '{item.item_id}' entity_type "
                    f"'{item.entity_type}' does not match envelope "
                    f"entity_type '{self.entity_type}'."
                )
        return self

    @property
    def pending(self) -> list[SMAHITLItem]:
        """Items not yet reviewed — choice is None."""
        return [i for i in self.items if i.choice is None]

    @property
    def pending_direct_edits(self) -> list[SMAHITLItem]:
        """
        Items where reviewer selected direct_edit but has not yet
        populated direct_edit_field_mapping. Blocks resolution.
        """
        return [
            i for i in self.items
            if i.is_direct_edit and i.direct_edit_field_mapping is None
        ]

    @property
    def gate_pending(self) -> list[SMAHITLItem]:
        """
        All items that block the pipeline gate: no choice yet, or direct_edit
        without a populated mapping. Used by :func:`check_gate_sma_hitl`.
        """
        return self.pending + self.pending_direct_edits

    @property
    def is_clear(self) -> bool:
        """True when the gate passes — every item reviewed and direct edits complete."""
        return len(self.gate_pending) == 0


