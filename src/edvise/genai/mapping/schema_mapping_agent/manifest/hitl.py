class SMARRunEvent(BaseModel):
    """
    One resolved SMA HITL item event.
    Appended to institutions/<institution_id>/run_log.json alongside
    IdentityAgent events — same file, different agent field.
    """

    model_config = ConfigDict(extra="forbid")

    timestamp: str
    resolved_by: str | None
    agent: str = "schema_mapping_agent"
    entity_type: str                    # "cohort" or "course"
    item_id: str
    target_field: str
    failure_mode: str
    choice: int
    option_id: str
    reentry: str                        # "terminal" or "direct_edit"


